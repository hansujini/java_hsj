package day3;

public class ForNumberEx1 {

	public static void main(String[] args) {
		/* 1부터 5까지 출력하는 코드는 for문으로 작성하세요.
		 * 반복횟수 : 5번, 숫자는 중요 1부터 5까지 (까지는 같다를 포함) 1씩 증가
		 * 출력예시
		 * 1 
		 * 2
		 * 3
		 * 4
		 * 5
		 * 
		 * for문에서는 의미가 없는 변수 i를 주로 사용함
		 * */ 
		
		int i;
		for ( i = 1; i <= 5; i++ )
			System.out.println(i+"입니다.");
		
	}

}
